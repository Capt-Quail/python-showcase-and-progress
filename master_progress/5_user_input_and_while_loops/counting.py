# Introducing while loops.

# For loops take a collection of items and executes a block of code once for
# each item in the collection. In contrast, while loops run as long as, or
# while, a certain condition is true. 
# The following loop counts from 1 to 5. 
current_number = 1
while current_number <= 5:
    print(current_number)
    current_number += 1